// 生成最近两个月内的日期
const getRecentDate = (daysAgo) => {
  const date = new Date()
  date.setDate(date.getDate() - daysAgo)
  return date.toISOString().slice(0, 19).replace('T', ' ')
}

const getFutureDate = (daysLater) => {
  const date = new Date()
  date.setDate(date.getDate() + daysLater)
  return date.toISOString().slice(0, 19).replace('T', ' ')
}

